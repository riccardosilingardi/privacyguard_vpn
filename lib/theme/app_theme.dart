  }

  /// Data text style using JetBrains Mono for technical metrics
  static TextStyle dataTextStyle({
    required bool isLight,
    double fontSize = 14,
    FontWeight fontWeight = FontWeight.w400,
  }) {
    final Color textColor =
        isLight ? textHighEmphasisLight : textHighEmphasisDark;
    return GoogleFonts.jetBrainsMono(
      fontSize: fontSize,
      fontWeight: fontWeight,
      color: textColor,
      letterSpacing: 0.0,
    );
  }

  /// Connection status colors for VPN states
  static Color getConnectionStatusColor(String status,
      {required bool isLight}) {
    switch (status.toLowerCase()) {
      case 'connected':
        return successLight;
      case 'connecting':
        return secondaryLight;
      case 'disconnected':
        return errorLight;
      default:
        return isLight ? textMediumEmphasisLight : textMediumEmphasisDark;
    }
  }

  /// ICR reward accent color with opacity variants
  static Color getRewardAccentColor({double opacity = 1.0}) {
    return secondaryLight.withValues(alpha: opacity);
  }
